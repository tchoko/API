    public void AddClient() {
        try {
            cli = clientDAO.Affiche();
            if (client != null) {
                client.removeAllItems();
            }

            Iterator<API_CLIENT1> itc = cli.iterator();
            while (itc.hasNext()) {
                API_CLIENT1 cl = itc.next();
                dcbmcli.addElement(cl.toString());
            }
           
            client.setModel(dcbmcli);

        } catch (SQLException e) {
            System.out.println("Exception" + e);
        }
    }
    
     public void AddAddress() {
        try {
            dep = adresseDAO.affiche();
            arr = adresseDAO.affiche();
            if (depart != null) {
                depart.removeAllItems();
                arrive.removeAllItems();
            }

            Iterator<API_ADRESSE1> itad1 = dep.iterator();
            Iterator<API_ADRESSE1> itad2 = arr.iterator();

            while (itad1.hasNext()) {
                API_ADRESSE1 ad1 = itad1.next();
                API_ADRESSE1 ad2 = itad2.next();
                dcbmdep.addElement(ad1.toString());
                dcbmarr.addElement(ad2.toString());

            }
            depart.setModel(dcbmdep);
            arrive.setModel(dcbmarr);

        } catch (SQLException e) {
            System.out.println("Exception" + e);
        }
    }
       public void Addtaxi() {
        try {
            tx = taxiDAO.Affiche();
            if (taxi != null) {
                taxi.removeAllItems();
            }
            Iterator<API_TAXI1> ittx = tx.iterator();
            while (ittx.hasNext()) {
                API_TAXI1 v = ittx.next();
                dcbmtx.addElement(v.toString());
            }
           
            taxi.setModel(dcbmtx);

        } catch (SQLException e) {
            System.out.println("Exception" + e);
        }
    }

    

