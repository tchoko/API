    private void validerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_validerActionPerformed
        // TODO add your handling code here:
        try {

            String dtdeb = date.getText();
            int jdeb = Integer.parseInt(dtdeb.substring(0, 2));
            int mdeb = Integer.parseInt(dtdeb.substring(3, 5));
            int adeb = Integer.parseInt(dtdeb.substring(6));

            LocalDate day = LocalDate.of(adeb, mdeb, jdeb);

            int kmtotal = Integer.parseInt(km.getText());
            Float acomptes = Float.parseFloat(acompte.getText());
            Float totals = Float.parseFloat(total.getText());
            int fkclient = client.getSelectedIndex();
            API_CLIENT1 client2 = cli.get(fkclient);
            int fktx = taxi.getSelectedIndex();
            API_TAXI1 tx1 = tx.get(fktx);
            int fkdeb = depart.getSelectedIndex();
            API_ADRESSE1 addresst = dep.get(fkdeb);
            int fkarr = arrive.getSelectedIndex();
            API_ADRESSE1 addressend = arr.get(fkarr);
            //API_LOCATION1(idloc, dateloc, kmtotal, acompte, total, id_deb, id_fin,  idtaxi, idclient
            API_LOCATION1 l = new API_LOCATION1(0, day, kmtotal, acomptes, totals, addresst.getIdadr(), addressend.getIdadr(), tx1.getIdtaxi(),client2.getIdclient());
            locationDAO.create(l);
            AddAddress();
            Addtaxi();
            AddClient();
            JOptionPane.showMessageDialog(this, "Location créé", "Succès", JOptionPane.INFORMATION_MESSAGE);
        } 
        catch (HeadlessException | NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERREUR", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(CreerLoc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_validerActionPerformed

